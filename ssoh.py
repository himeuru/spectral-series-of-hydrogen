names = ['Хансена — Стронга', 'Хэмпфри', 'Пфунда', 'Брэккета', 'Пашена', 'Бальмера', 'Лаймана']
for e1 in range(7,0,-1):
    print(f'-------Серия {names[7-e1]}-----------')
    for e2 in range(e1+1, e1+6):
        E=13.6*16* ( (e2**2-e1**2)/((e1**2)*(e2**2)) )
        print(f'E({e2}->{e1}) = {E}*10**-20 Дж')
        Nu = (100*E)/6.63
        print(f'ν({e2}->{e1}) = {Nu} ТГц')
        lambd = 3000000/Nu
        print(f'λ({e2}->{e1}) = {lambd} Å')
        print()
    
